	<b>How can I breed my tamaNOTchi?</b>
	<br><br>
	Both pets have to be old enough to breed, but the genders don't matter anymore. Pets are old enough starting at Stage 3, as seen in this diagram:
	<br><br>
	<img src="/includes/img/breeding.png" alt="breeding diagram">
