	<b>What is tamaNOTchi?</b>
	<br><br>
	tamaNOTchi are breedable pets that grow bigger as you feed them (also referred to as "clicks"). This project was inspired both by Tamagotchi and the original Kingdom of Knuffel feedable pets. 
	<br><br>
	Please be aware that tamaNOTchi is a highly experimental programming project. Things can and will break at any time. Sometimes, the site may even need to be taken offline temporarily to fix something. However, I will do my best to resolve any problems within a reasonable time frame. Because nothing is guaranteed, tamaNOTchi is completely free to use.
	<br><br>
	If you find any issues, please report them to me either through the Feedback link at the top or via PM on Gaia, Roliana, or recolor.me.
	<br><br>
	<b>How can I get my own tamaNOTchi?</b>
	<br><br>
	At this time, tamaNOTchi is exclusive to Gaia Online, Roliana, and recolor.me. You must have an account on one of those sites, and you must post in the thread (linked at the top of this site) to request a pet. A form will be provided in the first post.
	<br><br>
	<b>Why isn't my tamaNOTchi eating? / Why isn't the Clicks count going up?</b>
	<br><br>
	tamaNOTchi won't eat if it's not hungry. Give it a few minutes and try again.
	<br><br>
	<b>How can I put my tamaNOTchi on my site?</b>
	<br><br>
	Officially, tamaNOTchi only supports BBCode embeds at this time. That code can be found on your pet's profile page. If you know HTML and wish to embed it that way on your Tumblr or something, go for it! I want to see it!<br>
	I'll add official HTML embed codes shortly.
