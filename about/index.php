	<b>I want to change my owner name / tamaNOTchi's name or gender / tamaNOTchi's colors.</b>
	<br><br>
	Owner/tamaNOTchi names can be changed, yes. Contact me in the thread. Include a link to the original post in the thread where you first ordered your pet to prove it's yours.
	<br><br>
	The color of the pet itself cannot be changed. That is randomly generated.
	<br><br>
	As of now, tamaNOTchi's gender will only be changed under extreme circumstances; for example, if the system is only producing male pets and causing an imbalance. (Yes, this has happened before.)
	<br><br>
	The color of any ribbons is shared with the button colors.
	<br><br>
	The button colors and device colors can be changed. As with name changes, contact me in the thread with a link to your original request.
	<br><br>
	To avoid conflict, all changes must be requested using the same Gaia/Roliana/recolor.me account that you used to order your tamaNOTchi. For example, if you say you were banned and using a new account, or if you are using a mule, I cannot honor any change requests, since there is no way for me to know it's actually you. However, you are always welcome to order a new tamaNOTchi.
