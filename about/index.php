	Breeding must be facilitated through the respective threads on Gaia/Roliana/recolor.me where you ordered your pet. A form will be provided in the first post.
	<br><br>
	To avoid quarrels, if the other parent does not belong to you, both owners will be required to fill out the form.
	<br><br>
	All requests must be made using the same Gaia/Roliana/recolor.me account you ordered your pet with, or they will not be honored.
	<br><br>
	If you would like to breed but lack another parent, contact me through the thread and I will be more than happy to let you breed with one of my pets.
